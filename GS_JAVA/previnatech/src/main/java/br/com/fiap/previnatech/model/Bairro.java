package br.com.fiap.previnatech.model;

public class Bairro {
	
	public Long idBairro;
	public String nomeBairro;
	public String zonaBairro;
	
	public Long getIdBairro() {
		return idBairro;
	}
	
	public void setIdBairro(Long idBairro) {
		this.idBairro = idBairro;
	}
	
	public String getNomeBairro() {
		return nomeBairro;
	}
	
	public void setNomeBairro(String nomeBairro) {
		this.nomeBairro = nomeBairro;
	}

	public String getZonaBairro() {
		return zonaBairro;
	}

	public void setZonaBairro(String zonaBairro) {
		this.zonaBairro = zonaBairro;
	}
	
	
}
